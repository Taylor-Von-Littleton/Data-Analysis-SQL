-- Remove Duplicates

--creates Temp table with Partioned columns ordered by unique number as row num
WITH RowNumCTE AS(
Select *,--',' needed to continue creation of temp table
	ROW_NUMBER() OVER (
	PARTITION BY ParcelID,
				 PropertyAddress,
				 SalePrice,
				 SaleDate,
				 LegalReference
				 ORDER BY
					UniqueID
					) row_num

From PortfolioProject.dbo.NashvilleHousing
--order by ParcelID
)
SELECT *
From RowNumCTE
Where row_num > 1
Order by PropertyAddress


