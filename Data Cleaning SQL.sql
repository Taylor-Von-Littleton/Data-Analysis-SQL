-- Change Y and N to Yes and No in "Sold as Vacant" field


--Displays the number of responses for SoldAsVacant for (Y, N, Yes, No).
Select Distinct(SoldAsVacant), Count(SoldAsVacant)
From PortfolioProject.dbo.NashvilleHousing
Group by SoldAsVacant
order by 2
--check after update to verify changes



Select SoldAsVacant
, CASE When SoldAsVacant = 'Y' THEN 'Yes'--When respone is 'Y' change it to 'Yes' | CASE -opens temporary conditional statement
	   When SoldAsVacant = 'N' THEN 'No'--When response is 'N'change it to 'No'
	   ELSE SoldAsVacant--Otherwise leave it be.
	   END--Closes conditional statement
From PortfolioProject.dbo.NashvilleHousing

--Permanently update Y to Yes and N to No 
Update NashvilleHousing
SET SoldAsVacant = CASE When SoldAsVacant = 'Y' THEN 'Yes'
	   When SoldAsVacant = 'N' THEN 'No'
	   ELSE SoldAsVacant
	   END






